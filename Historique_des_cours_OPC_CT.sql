SELECT
	val.IEX_VAL,
	val.VAL_LIB_ABR,
	datca.DAT_JOU,
	COU,
	COU_UNIT	
FROM
	DWH.FACT_CVA cou
JOIN DIM_VAL val ON
	val.val_key = cou.val_key
	AND val.VAL_DER_VER_SWI = 'Y' /*AND val.IEX_VAL='FR0000053951'*/
	AND val.val_str_cou IS NULL
JOIN DIM_TCV tcv ON
	tcv.TCV_KEY = cou.TCV_KEY
JOIN DIM_DAT_CAL datca ON
	datca.DAT_KEY = cou.DAT_DEB_KEY
JOIN DIM_DAT_CAL datcafin ON
	DATCAFIN.DAT_KEY = cou.DAT_FIN_KEY
WHERE
	datca.DAT_JOU BETWEEN '11/10/2017' AND '17/06/2020'
	AND val.IEX_VAL IN ('FR0011683077','FR0010705491','FR0010814806','FR0011683093','FR0011521525','FR0010377564','FR0013428539')
ORDER BY
	val.VAL_LIB_ABR DESC,
	datca.DAT_JOU DESC;
